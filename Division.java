class Division{

	public static void main(String division[]){
		
		div(34, 30);
		div(56, 56);
		div(45, 12);
		div(56, 34);
		div(78, 67);
		div(45, 20);
		div(25, 22);
		div(12, 1);
		div(78, 32);
		div(23, 15);
		
		System.out.println("---------------------------");
	
		div(452, 5, 2);
		div(78, 6, 3);
		div(145, 8, 4);
		div(56, 9, 8);
		div(78, 8, 3);
		div(45, 9, 6);
		div(78, 9, 4);
		div(78, 7, 1);
		div(90, 8, 4);
		div(89, 6, 2);
	}
	
	public static void div(double g, double h){
		
		System.out.println(g/h);
	}
	
	public static void div(int g, int h, int i){
		
		System.out.println((g/h)/i);
	}
}


