class Multiplication{

	public static void main(String multiplication[]){
		
		
		mul(12.50, 13);
		mul(11, 88);
		mul(33, 43);
		mul(43,56);
		mul(25, 67);
		mul(45, 67);
		mul(21, 22);
		mul(1, 3);
		mul(60, 80);
		mul(2, 2);
	
	System.out.println("---------------------------");
	
		mul(12.50, 13, 45);
		mul(11, 88, 3);
		mul(33, 43, 4);
		mul(43,56, 78);
		mul(25, 67, 5);
		mul(45, 67, 76);
		mul(21, 22, 7);
		mul(1, 3, 5);
		mul(60, 80, 9);
		mul(2, 2, 2);
	}
	
	
	public static void mul(double e, int f){
		
		System.out.println(e*f);
	}
	
	public static void mul(double e, int f, int g){
		
		System.out.println(e*f*g);
	}
}



